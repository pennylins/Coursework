#四則運算
n1=int(input("請輸入數字1:"))
n2=int(input("請輸入數字2:"))
op=input("請輸入+, - , * ,/: ")
if op=="+":
    print(n1+n2)
elif op=="-":
    print(n1-n2)
elif op=="*":
    print(n1*n2)
elif op=="/":
    print(n1/n2)
else:
    print("不支援的運算")

